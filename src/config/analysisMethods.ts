export async function brainFeaturesAnalysis(data: any[][]): Promise<any> {
    // 模擬呼叫 API
    return new Promise((resolve) =>
        setTimeout(() => resolve({analysis: 'brainFeatures', dataLength: data.length}), 1000)
    );
}

